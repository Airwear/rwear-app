<h1>Politique de Confidentialité</h1>

<p>Cette politique de confidentialité décrit comment nous collectons, utilisons et protégeons vos informations personnelles lorsque vous utilisez notre application <strong>{{ config('app.name') }}</strong>. Nous nous engageons à respecter la confidentialité de vos données conformément aux règlements applicables, notamment le Règlement Général sur la Protection des Données (RGPD).</p>

<h2>1. Informations collectées</h2>
<p>Nous collectons les types d'informations suivants lors de l'utilisation de notre application :</p>
<ul>
    <li><strong>Données personnelles fournies volontairement</strong> : nom, adresse e-mail, numéro de téléphone, etc.</li>
    <li><strong>Données de navigation</strong> : adresse IP, type de navigateur, système d'exploitation, préférences de langue, etc.</li>
    <li><strong>Données collectées via des cookies</strong> et autres technologies similaires pour améliorer l'expérience utilisateur et analyser l'utilisation de notre application.</li>
</ul>

<h2>2. Utilisation des informations</h2>
<p>Les données collectées sont utilisées pour :</p>
<ul>
    <li>Fournir et améliorer nos services.</li>
    <li>Personnaliser l'expérience utilisateur.</li>
    <li>Répondre aux questions et demandes des utilisateurs.</li>
    <li>Assurer la sécurité de l'application et prévenir la fraude.</li>
    <li>Se conformer à nos obligations légales et réglementaires.</li>
</ul>

<h2>3. Partage des données</h2>
<p>Nous ne partageons vos données personnelles avec des tiers que dans les cas suivants :</p>
<ul>
    <li>Si cela est nécessaire pour fournir les services demandés, comme des prestataires de services (hébergement, maintenance, etc.).</li>
    <li>Si la loi l'exige (ex. : obligation légale, requête judiciaire, etc.).</li>
    <li>Si vous avez expressément consenti au partage de vos données.</li>
</ul>

<h2>4. Durée de conservation des données</h2>
<p>Nous conservons vos données personnelles uniquement pour la durée nécessaire à la réalisation des finalités pour lesquelles elles ont été collectées, sauf si une période de conservation plus longue est exigée ou autorisée par la loi.</p>

<h2>5. Vos droits</h2>
<p>Conformément au RGPD, vous disposez des droits suivants concernant vos données personnelles :</p>
<ul>
    <li><strong>Droit d'accès</strong> : vous avez le droit de savoir quelles données nous détenons à votre sujet.</li>
    <li><strong>Droit de rectification</strong> : vous pouvez demander la correction de vos données personnelles si elles sont inexactes ou incomplètes.</li>
    <li><strong>Droit à l'effacement</strong> : vous pouvez demander la suppression de vos données dans certaines circonstances.</li>
    <li><strong>Droit à la portabilité</strong> : vous avez le droit de recevoir vos données dans un format structuré et de les transmettre à un autre responsable de traitement.</li>
    <li><strong>Droit à la limitation du traitement</strong> : vous pouvez demander la limitation du traitement de vos données dans certaines situations.</li>
    <li><strong>Droit d'opposition</strong> : vous pouvez vous opposer au traitement de vos données pour des raisons légitimes.</li>
</ul>
<p>Pour exercer ces droits, veuillez nous contacter à l'adresse suivante : <strong>contact@rwear-sport.com</strong>.</p>

<h2>6. Sécurité des données</h2>
<p>Nous mettons en place des mesures de sécurité techniques et organisationnelles appropriées pour protéger vos données personnelles contre tout accès non autorisé, perte ou altération. Cependant, aucune méthode de transmission sur Internet ou de stockage électronique n'est totalement sécurisée, et nous ne pouvons garantir une sécurité absolue.</p>

<h2>7. Cookies</h2>
<p>Notre application utilise des cookies pour améliorer l'expérience utilisateur. Vous avez la possibilité de refuser l'utilisation de cookies en modifiant les paramètres de votre navigateur. Toutefois, cela pourrait limiter certaines fonctionnalités de notre application.</p>

<h2>8. Modifications de cette politique</h2>
<p>Nous pouvons mettre à jour cette politique de confidentialité de temps à autre pour refléter les modifications de nos pratiques ou les changements réglementaires. Nous vous informerons de toute modification en publiant la nouvelle politique sur cette page et, si nécessaire, en vous contactant directement.</p>

